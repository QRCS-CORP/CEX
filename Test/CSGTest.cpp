	void CSGTest::Reseed()
	{
		const size_t SMPLEN = 10240;
		const size_t SMPCNK = 1024;

		CSG gen(ShakeModes::SHAKE128, Providers::CSP, false);
		Cipher::SymmetricKeySize ks = gen.LegalKeySizes()[0];
		std::vector<uint8_t> key(ks.KeySize(), 0x32);
		std::vector<uint8_t> iv(ks.IVSize(), 0x64);
		std::vector<uint8_t> otp(SMPLEN);
		SymmetricKey kp(key, iv);
		size_t i;
		size_t j;

		// set a low reseed threshold
		gen.ReseedThreshold() = SMPCNK;

		for (i = 0; i < TEST_CYCLES; ++i)
		{
			try
			{
				// re-initialize
				gen.Initialize(kp);

				// generator will re-seed itself 10x on every test cycle
				for (j = 0; j < SMPLEN / SMPCNK; ++j)
				{
					gen.Generate(otp, j * SMPCNK, SMPCNK);
				}
			}
			catch (CryptoException &ex)
			{
				throw TestException(std::string("Reseed"), gen.Name(), ex.Message());
			}
		}
	}

	void CSGTest::Stress()
	{
		SHAKE kdf(ShakeModes::SHAKE256);
		CSG gen(ShakeModes::SHAKE256, Providers::None);
		Cipher::SymmetricKeySize ks = kdf.LegalKeySizes()[0];
		std::vector<uint8_t> name(0);
		std::vector<uint8_t> otp1;
		std::vector<uint8_t> otp2;
		std::vector<uint8_t> key(ks.KeySize());
		SecureRandom rnd;
		size_t i;

		otp1.reserve(MAXM_ALLOC);
		otp2.reserve(MAXM_ALLOC);

		for (i = 0; i < TEST_CYCLES; ++i)
		{
			const size_t SMPLEN = static_cast<size_t>(rnd.NextUInt32(MAXM_ALLOC, MINM_ALLOC));
			otp1.resize(SMPLEN);
			otp2.resize(SMPLEN);
			rnd.Generate(key, 0, key.size());

			// generate with the kdf
			kdf.Initialize(key, m_custom, name);
			kdf.Generate(otp1, 0, SMPLEN);
			// generate with the gen
			SymmetricKey kp(key, m_custom, name);
			gen.Initialize(kp);
			gen.Generate(otp2, 0, SMPLEN);

			if (otp1 != otp2)
			{
				throw TestException(std::string("Stress"), gen.Name(), std::string("Transformation output is not equal! -TS1"));
			}
		}
	}
