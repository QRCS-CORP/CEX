	std::vector<byte> tmpk(RLWE_SEED_SIZE + 2);
	size_t i;
	size_t j;
	byte a;
	byte b;

	MemoryTools::Copy(Seed, 0, tmpk, 0, RLWE_SEED_SIZE);
	tmpk[RLWE_SEED_SIZE] = Nonce;

