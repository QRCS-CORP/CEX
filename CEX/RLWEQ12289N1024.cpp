void RLWEQ12289N1024::BitRevVector(std::array<ushort, RLWE_N> &P)
{
	ushort i;
	ushort r;
	ushort tmp;

	for (i = 0; i < RLWE_N; ++i)
	{
		r = BitRevTable[i];

		if (i < r)
		{
			tmp = P[i];
			P[i] = P[r];
			P[r] = tmp;
		}
	}
}

